                        helper.saveCMSLog(uid, JsonConvert.SerializeObject(new { data = ids, contents = "" }), domainurl + "Lang/Del_Lang", ip, tid, "Xoá ngôn ngữ " + string.Join(",", ids), "Lang");
                        #endregion
                        await db.SaveChangesAsync();
                        foreach (string strPath in paths)
                        {
                            bool exists = Directory.Exists(strPath);
                            if (exists)
                                System.IO.File.Delete(strPath);
                        }
