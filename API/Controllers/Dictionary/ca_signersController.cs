            catch (DbEntityValidationException e)
            {
                string contents = helper.getCatchError(e, null);
                helper.saveLog(uid, name, JsonConvert.SerializeObject(new { data = contents }), domainurl + "Signers/Add_Signers", ip, tid, "Lỗi khi thêm người ký", 0, "Signers");
