        [HttpPost]
        public async Task<HttpResponseMessage> add_hrm_paycheck()
        {
            var identity = User.Identity as ClaimsIdentity;
            if (identity == null)
            {
                return Request.CreateResponse(HttpStatusCode.OK, new { ms = "Bạn không có quyền truy cập chức năng này!", err = "1" });
            }
            string fdpaycheck = "";
            IEnumerable<Claim> claims = identity.Claims;
            string ip = getipaddress();
            string name = claims.Where(p => p.Type == "fname").FirstOrDefault()?.Value;
            string tid = claims.Where(p => p.Type == "tid").FirstOrDefault()?.Value;
            string dvid = claims.Where(p => p.Type == "dvid").FirstOrDefault()?.Value;
            string uid = claims.Where(p => p.Type == "uid").FirstOrDefault()?.Value;

            string domainurl = HttpContext.Current.Request.Url.Scheme + "://" + HttpContext.Current.Request.Url.Host + ":" + HttpContext.Current.Request.Url.Port + "/";

            try
            {
                using (DBEntities db = new DBEntities())
                {
                    if (!Request.Content.IsMimeMultipartContent())
                    {
                        throw new HttpResponseException(HttpStatusCode.UnsupportedMediaType);
                    }

                    string root = HttpContext.Current.Server.MapPath("~/Portals");

                    var provider = new MultipartFormDataStreamProvider(root);

                    // Read the form data and return an async task.
                    var task = Request.Content.ReadAsMultipartAsync(provider).
                    ContinueWith<HttpResponseMessage>(t =>
                    {
                        if (t.IsFaulted || t.IsCanceled)
                        {
                            Request.CreateErrorResponse(HttpStatusCode.InternalServerError, t.Exception);
                        }
                        fdpaycheck = provider.FormData.GetValues("hrm_paycheck").SingleOrDefault();
                        hrm_paycheck paycheck = JsonConvert.DeserializeObject<hrm_paycheck>(fdpaycheck);
                        bool super = claims.Where(p => p.Type == "super").FirstOrDefault()?.Value == "True";

                        var checkBarcode = db.hrm_paycheck.Where(a => a.paycheck_code == paycheck.paycheck_code && a.paycheck_type==2).FirstOrDefault();



                        if (checkBarcode != null)
                        {
                            return Request.CreateResponse(HttpStatusCode.OK, new { ms = "Mã số đã tồn tại! Vui lòng nhập lại", err = "1" });
                        }

                        paycheck.organization_id = super ? 0 : int.Parse(dvid);
                        paycheck.created_by = uid;
                        paycheck.created_date = DateTime.Now;
                        paycheck.created_ip = ip;
                        paycheck.created_token_id = tid;
                        db.hrm_paycheck.Add(paycheck);
                        db.SaveChanges();

                        #region add hrm_log
                        if (helper.wlog)
                        {

                            hrm_log log = new hrm_log();
                            log.title = "Thêm mẫu phiếu lương " + paycheck.paycheck_name;

                            log.log_module = "paycheck";
                            log.log_type = 0;
                            log.id_key = paycheck.paycheck_id.ToString();
                            log.created_date = DateTime.Now;
                            log.created_by = uid;
                            log.created_token_id = tid;
                            log.created_ip = ip;
                            db.hrm_log.Add(log);
                            db.SaveChanges();

                        }
                        #endregion
                        return Request.CreateResponse(HttpStatusCode.OK, new { err = "0" });
                    });
                    return await task;
                }
            }
            catch (DbEntityValidationException e)
            {
                string contents = helper.getCatchError(e, null);
                helper.saveLog(uid, name, JsonConvert.SerializeObject(new { data = fdpaycheck, contents }), domainurl + "hrm_paycheck/Add_paycheck", ip, tid, "Lỗi khi thêm mẫu phiếu lương", 0, "mẫu phiếu lương");
                if (!helper.debug)
                {
                    contents = "";
                }
                Log.Error(contents);
                return Request.CreateResponse(HttpStatusCode.OK, new { ms = contents, err = "1" });
            }
            catch (Exception e)
            {
                string contents = helper.ExceptionMessage(e);
                helper.saveLog(uid, name, JsonConvert.SerializeObject(new { data = fdpaycheck, contents }), domainurl + "hrm_paycheck/Add_paycheck", ip, tid, "Lỗi khi thêm mẫu phiếu lương", 0, "mẫu phiếu lương  ");
                if (!helper.debug)
                {
                    contents = "";
                }
                Log.Error(contents);
                return Request.CreateResponse(HttpStatusCode.OK, new { ms = contents, err = "1" });
            }
        }
