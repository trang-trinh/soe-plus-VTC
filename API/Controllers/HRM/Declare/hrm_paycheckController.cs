                string domainurl = HttpContext.Current.Request.Url.Scheme + "://" + HttpContext.Current.Request.Url.Host + ":" + HttpContext.Current.Request.Url.Port + "/";
                try
                {
                    using (DBEntities db = new DBEntities())
                    {
                        var das = await db.hrm_paycheck.Where(a => id.Contains(a.paycheck_id)).ToListAsync();
                        List<string> paths = new List<string>();
                        if (das != null)
                        {
                            List<hrm_paycheck> del = new List<hrm_paycheck>();
                            foreach (var da in das)
                            {

                                del.Add(da);
                                var das1 = await db.hrm_paycheck.Where(a => a.parent_id==da.paycheck_id ).ToListAsync();
                                foreach (var item in das1)
                                {
                                    del.Add(item);
                                }
                                #region add hrm_log
                                if (helper.wlog)
                                {

                                    hrm_log log = new hrm_log();
                                    log.title = "Xóa mẫu phiếu lương " + da.paycheck_name;

                                    log.log_module = "paycheck";
