                    return Request.CreateResponse(HttpStatusCode.OK, new { ms = contents, err = "1" });
                }
                catch (Exception e)
                {
                    string contents = helper.ExceptionMessage(e);
                    helper.saveLog(uid, name, JsonConvert.SerializeObject(new { data = id, contents }), domainurl + "smart_report/Delete_smart_report", ip, tid, "Lỗi khi xoá Template báo cáo", 0, "smart_report");
                    if (!helper.debug)
